  ElevationScale(5),
  SlideOut(6),
  SlideOutHorizontally(7),
  SlideOutVertically(8),
  ScaleOut(9),
  ShrinkOut(10),
  ShrinkHorizontally(11),
  ShrinkVertically(12)
}
